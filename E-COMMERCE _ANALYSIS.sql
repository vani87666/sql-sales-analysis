--Q1: Total Revenue

--👉 Hint:

--JOIN orders + order_details
SELECT SUM(price * quantity) AS total_revenue
FROM orders o
INNER JOIN order_details od
ON o.order_id = od.order_id;

--Q2: Revenue per customer

--👉 customers + orders + order_details

SELECT c.name,
       SUM(od.price * od.quantity) AS total_revenue
FROM customers c
JOIN orders o 
  ON c.customer_id = o.customer_id
JOIN order_details od 
  ON o.order_id = od.order_id
GROUP BY c.name
ORDER BY total_revenue DESC;

--Q3: Top 3 customers by spending

SELECT c.name,
       SUM(od.price * od.quantity) AS total_spent
FROM customers c
JOIN orders o 
  ON c.customer_id = o.customer_id
JOIN order_details od 
  ON o.order_id = od.order_id
GROUP BY c.name
ORDER BY total_spent DESC
LIMIT 3;

--Q4:

--👉 Customers with NO orders

--(Hint: LEFT JOIN + NULL)

SELECT c.name
FROM customers c
LEFT JOIN orders o 
  ON c.customer_id = o.customer_id
WHERE o.order_id IS NULL;


--Q5: Most Sold Product
SELECT product, SUM(quantity) AS total_sold
FROM order_details
GROUP BY product
ORDER BY total_sold DESC limit 1;


--Q6: Monthly Revenue
SELECT MONTH(o.order_date) AS month,
       SUM(od.price * od.quantity) AS total_revenue
FROM orders o
JOIN order_details od 
  ON o.order_id = od.order_id
GROUP BY MONTH(o.order_date)
ORDER BY total_revenue DESC;

--🔥 Q7:

👉 City-wise revenue

SELECT c.city,
       SUM(od.price * od.quantity) AS Total_revenue
FROM customers c
JOIN orders o 
  ON c.customer_id = o.customer_id
JOIN order_details od 
  ON o.order_id = od.order_id
GROUP BY c.city
ORDER BY Total_revenue DESC;

--🎯 Q8: Customer Lifetime Value

--👉 Meaning:

--Total revenue generated by each customer (across all orders)

SELECT c.name,
       SUM(od.price * od.quantity) AS customer_lifetime_value
FROM customers c
JOIN orders o 
  ON c.customer_id = o.customer_id
JOIN order_details od 
  ON o.order_id = od.order_id
GROUP BY c.name
ORDER BY customer_lifetime_value DESC;
