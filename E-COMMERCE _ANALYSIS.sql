);

-- Orders Table
CREATE TABLE orders (                       
    order_id INT PRIMARY KEY,
    customer_id INT,
    order_date DATE
);


-- Order Details Table
CREATE TABLE order_details (
    order_id INT,
    product VARCHAR(50),
    quantity INT,
    price INT
);
